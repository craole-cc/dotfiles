#!/bin/sh

#> Install <#
if ! weHave lf; then
    if weHave paru; then
        Pin lf
    elif weHave choco; then
        choco install lf
    elif weHave winget; then
        winget install lf
    else
        cargo install lf
    fi
fi

#> Verify Instalation <#
weHave lf || return
"$NeedForSpeed" || weHave --report version lf >>"$DOTS_loaded_apps"

# --> Open Dir in LF
lfcd() {
    tmp="$(mktemp)"
    lf -last-dir-path="$tmp" "$@"
    if [ -f "$tmp" ]; then
        dir="$(cat "$tmp")"
        rm -f "$tmp" >/dev/null
        [ -d "$dir" ] && [ "$dir" != "$(pwd)" ] && cd "$dir" || exit
    fi
}

[ "$Shell" = zsh ] &&
    bindkey -s '^o' 'lfcd\n' # >>= ctrl + o
[ "$Shell" = bash ] &&
    bind "\C-o":lfcd # >>= ctrl + o

